public class Uses_Math_Class3 {
	public static void main(java.lang.String[] args) {
		byte POWEROF = 4;
		byte NUMBER = 2;
		double POWER = java.lang.Math.pow(NUMBER, POWEROF);
		java.lang.System.out.print("\u001B[2J");
		java.lang.System.out.print("\u001B[H");
		java.lang.System.out.print("\u001B[34m" + POWER + "\u001B[0m");
		java.lang.System.out.println();
	}
}
